x = 6
y = 5
z = 7

print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")

#División al piso
print(f"{z} // {y} = {z//y}")

#Módulo, si el residuo es cero la división en par de lo contrario es impar
print(f"{z} % {y} = {z%y}")

#Elevado
print(f"{z} ** {y} = {z**y}")

#Raiz cuadrada
print(f"{x} **0.5 = {x**0.5}")